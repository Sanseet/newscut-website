function applyLanguage(lang) {
  document.documentElement.lang = lang;
  document.body.setAttribute("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const value = I18N[lang] && I18N[lang][key];
    if (value !== undefined) {
      el.innerHTML = value;
    }
  });

  // Toggle button state
  document.querySelectorAll(".lang-btn").forEach(btn => {
    const isActive = btn.dataset.lang === lang;
    btn.classList.toggle("active", isActive);
    btn.setAttribute("aria-pressed", isActive ? "true" : "false");
  });

  try { localStorage.setItem("newscut.lang", lang); } catch (e) {}
}

document.addEventListener("DOMContentLoaded", () => {
  // Year stamp
  const y = document.getElementById("year");
  if (y) y.textContent = new Date().getFullYear();

  // Restore saved language, else auto-detect from browser
  let saved = null;
  try { saved = localStorage.getItem("newscut.lang"); } catch (e) {}
  const browser = (navigator.language || "en").toLowerCase().startsWith("hi") ? "hi" : "en";
  const initial = saved || browser;
  applyLanguage(initial);

  // Wire toggle buttons
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
  });
});
